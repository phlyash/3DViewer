package com.cgvsu.math.Points;

public class Point3D {
    private int x;
    private int y;
    private float z;

    public Point3D(int x, int y, float z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public float getZ() {
        return z;
    }
}
